export function colorSwitch (color) {
  switch (color) {
    case 'black':
      return '#1A1B1E'
    case 'yellow':
      return '#F59F00'
    case 'gray':
      return '#495057'
    case 'blue':
      return '#1C7ED6'
    case 'brown':
      return '#F76707'
    case 'red':
      return '#F03E3E'
    case 'green':
      return '#37B24D'
    default:
      return '#DCDCDC'
  }
}
